import { G1, G2, Hx, Hy, theCurveN } from "../src/params";


test('params::curve', () => {
  const n = "115792089210356248762697446949407573529996955224135760342422259061068512044369";
  const G1x = "48439561293906451759052585252797914202762949526041747995844080717082404635286";
  const G1y = "36134250956749795798585127919587881956611106672985015071877198253568414405109";
  const G2x = "47740058544123975345659045565480806942512691644523753334364842458428569601470";
  const G2y = "33278495164293995155288051613467782372221264644249322665888369174287785374281";
  const hx = "47740058544123975345659045565480806942512691644523753334364842458428569601470";
  const hy = "33278495164293995155288051613467782372221264644249322665888369174287785374281";
  expect(BigInt(n)).toEqual(theCurveN);
  expect(BigInt(G1x)).toEqual(G1.x);
  expect(BigInt(G1y)).toEqual(G1.y);
  expect(BigInt(G2x)).toEqual(G2.x);
  expect(BigInt(G2y)).toEqual(G2.y);
  expect(BigInt(hx)).toEqual(Hx);
  expect(BigInt(hy)).toEqual(Hy);
});

